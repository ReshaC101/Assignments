package day4.basic;

public class LiteralsDemo {
    public static void main(String[] args) {

        //1. Integers
        byte b = 127;
        short s = 32767;
        int i = 2147483647;
        long l = 2147483647;
        long l2= 2147483647L;

        //2.Floating
        float f = 32.45F;
        double d = 32.45;

        //3. Boolean
        boolean bl1 = true;
        boolean bl2 = false;

        //4. Characters
        char ch1 = 'a';
        char ch2 = '1';
        char ch3 = '\u0000';

        //5. Strings
        String s1 ="hello";
        String s2 = "abc2@23";
        String s3 = "Hey!! I am Resha :)";

        //6. Null Literals
        String s4 = null;
        //this is object in Java
        LiteralsDemo ob = new LiteralsDemo();

    }
}
