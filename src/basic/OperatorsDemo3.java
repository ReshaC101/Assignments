package day4.basic;

public class OperatorsDemo3 {
    public static void main(String[] args) {

        //Incrementation and Decrementation
        // a++ ++a, a-- --a

        int a = 10;
        System.out.println(a);
        ++a;
        System.out.println(a);

        int x = 20;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);
        System.out.println(++x);


    }
}
